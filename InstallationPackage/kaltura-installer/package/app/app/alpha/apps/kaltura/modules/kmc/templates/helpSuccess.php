						</p>
						<h5>Transcoder Selection</h5>
						<p>
							Kaltura uses a variety of transcoding tools both open source and commercial. Commercial transcoders typically provide 
							improved quality and smaller file sizes which result in smoother playback and less buffering events.
						</p>
						<h5>Available Encoding Formats:</h5>
						<ul>
							<li>Standard Web Quality FLV (Bitrate: 400kbps)&ndash; minimal in size, loads fast with minimal buffering events, best for videos with little movement. Appropriate for 750kbps connection</li>
							<li>Good Web Quality FLV (Bitrate: 800kbps)&ndash; presents a good balance between file size and quality. Appropriate for 1500kbps connection</li>
							<li>Excellent Web Quality FLV (Bitrate: 1200kbps)&ndash; may result in a relatively large file, appropriate for videos with a lot of movement. Appropriate for 3000kbps connection and above</li>
							<li>HD Content H264 &ndash; maximum quality. May create a long loading time & buffering events when playing online. Could be used for download for offline viewing.</li>
						</ul>
						<h5>Remixing Quality</h5>
						<p>
							If you are uploading videos that are intended for remixing and editing with Kaltura’s online editing tools, the Kaltura servers will optimize the settings to support remixing. 
							If you are uploading videos that are intended for viewing only, it is recommended to keep this unchecked.
						</p>
						
						<div class="dataTitle" id="contentSection186"><a href="#main">Back to top</a><h4><span>1.8.6</span>Aspect Ratio</h4></div>
