							<br />
							(i) 4:3 &ndash; normal screen; and
							<br />
							(ii) 16:9 &ndash; wide screen. When playing a wide screen video in a normal (4:3) screen there will be black margins on the video. 
							In most cases, keeping the original ratio is the best configuration.
						</p>
						
