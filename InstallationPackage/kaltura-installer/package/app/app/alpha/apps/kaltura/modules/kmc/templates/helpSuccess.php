						<p>
							The main consideration in selecting your transcoding settings is the tradeoff between size and quality. 
							Higher quality videos require larger files that may cause buffering events during playback.  
							Additional parameters that need to be taken into consideration are the quality of the source video file uploaded, 
							the amount of movement in the video (higher movement requires higher quality, for example sports videos) 
							and the size of the player in which the video will be played.
						</p>
