							<dt>Entry</dt>
							<dd>Any item of content appearing in your account, such as video, video mix, audio or image.</dd>
							<dt>Tag</dt>
							<dd>A non-hierarchical keyword or term assigned to an entry to help describe it and allow it to be found again through a search. </dd>
							<dt>Admin Tags</dt>
							<dd>A non-hierarchical keyword or term assigned to an entry to help describe it and allow it to be found again through a search by an administrator. Admin tags can only be added by the system administrator.</dd>
							<dt>Video Mix</dt>
							<dd>A video made up of two or more entries.</dd>
							<dt>Moderation Status</dt>
							<dd>This is the status of moderation for each entry.  There are three possible statuses. By default all entries are given the status "approved", unless flagged by a user as containing inappropriate content, or changed by an administrator:
								<dl>
									<dt>Approved</dt>
									<dd>The default status for all entries OR content that has been flagged, reviewed and manually approved by a moderator.</dd>
									<dt>Flagged for Review</dt>
									<dd>The default status for all entries OR content that has been flagged, reviewed and manually approved by a moderator.</dd>
									<dt>Rejected</dt>
									<dd>Content that has been rejected by a moderator and will no longer appear on the site. The content remains in the system, marked as rejected, and moderators can continue to change the status in the future.</dd>
								</dl>
							</dd>
							<dt>Player loads</dt>
							<dd>The number of times the player loads onto a web page &ndash; i.e. the number of times a page containing a player is loaded (impressions).</dd>
							<dt>Plays</dt>
							<dd>The number of times an entry is played. Plays are counted when a user clicks play or when a video set to auto-play begins to run.</dd>
							<dt>Metadata</dt>
							<dd>Descriptive information about an entry or playlist.</dd>
							<dt>Thumbnail</dt>
							<dd>A small version of an image or an image representing a video, used for preview purposes.</dd>
							<dt>Playlist</dt>
							<dd>A list of entries that are streamed one after the other.</dd>
							<dt>Rating</dt>
							<dd>The average rating for a video is 1-5 stars. Note that this is only relevant for sites using the Kaltura rating widget or rating API.</dd>
							<dt>Vote Count</dt>
							<dd>The amount of votes accumulated for a specific entry.  Note that this is only relevant for sites using the Kaltura voting widget or voting API.</dd>
							<dt>MRSS</dt>
							<dd>Media RSS (MRSS) is an RSS module used for syndicating multimedia files (audio, video, image) in RSS feeds.</dd>
							<dt>Rule</dt>
							<dd>A set of parameters that determine which entries appear on a playlist.</dd>
						</dl>	
						
