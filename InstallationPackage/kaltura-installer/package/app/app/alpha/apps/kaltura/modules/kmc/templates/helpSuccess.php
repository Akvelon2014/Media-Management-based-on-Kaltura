						<p>The entry search area enables you to search for specific entries existing in your account. When you click the Entries tab in the main menu you'll first see the basic search option. Here you can search for any word in the entry name, tags or both. Clicking "Additional Filters" opens further search options including the "Admin Tag Filter" enabling you to search for words in the Admin tags, "Media Type" allowing you to select video, video mix, audio or image entries and "Status" which allows you to select approved, flagged or rejected content. By clicking the calendar icons on the "Created between" field you can also select a date range on which to run your query. You can use one, several or all fields for your query. When more than one field is used, the logical relationship between fields is an "AND" relationship.</p>
						
